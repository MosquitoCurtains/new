            ELSE 'mapped'::text
        END AS mapping_status
   FROM ((public.legacy_line_items li
     LEFT JOIN public.legacy_product_mapping pm ON ((li.product_name = pm.legacy_product_name)))
     LEFT JOIN public.products p ON ((pm.new_product_sku = p.sku)));


